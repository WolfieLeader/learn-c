#include <stdio.h>

void relationalInfo()
{
    int a = 3, b = 5;
    printf("\n Relational Operators \n");
    printf("\n\t a = %d, b = %d \n", a, b);
    printf("\n\t a == b (a is equal to b) = %d \n", a == b);
    printf("\n\t a != b (a is not equal to b) = %d \n", a != b);
    printf("\n\t a > b (a is greater than b) = %d \n", a > b);
    printf("\n\t a < b (a is less than b) = %d \n", a < b);
    printf("\n\t a >= b (a is greater than or equal to b) = %d \n", a >= b);
    printf("\n\t a <= b (a is less than or equal to b) = %d \n", a <= b);
}